package com.ef.common;

public class BlockEntry {
    private String ip;
    private String comment;
    
    public BlockEntry() {
        
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
